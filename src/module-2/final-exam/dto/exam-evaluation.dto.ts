import { Transform, Type } from 'class-transformer';
import {
  IsBoolean,
  IsEnum,
  IsInt,
  IsNumber,
  IsOptional,
  IsString,
  IsUUID,
  Max,
  MaxLength,
  Min,
} from 'class-validator';

import { ExamAttemptStatus, ExamVerdict } from '../types/final-exam.type';

const trim = ({ value }: { value: unknown }) =>
  typeof value === 'string' ? value.trim() : value;

export enum EvaluationQueueSortBy {
  TIME_IN_QUEUE = 'timeInQueue',
  SUBMISSION_DATE = 'submissionDate',
  STUDENT_NAME = 'studentName',
  STATUS = 'status',
}

export enum EvaluationQueueSortOrder {
  ASC = 'ASC',
  DESC = 'DESC',
}

export class EvaluationQueueQueryDto {
  @IsOptional()
  @IsEnum(ExamAttemptStatus)
  status?: ExamAttemptStatus;

  @IsOptional()
  @Transform(trim)
  @IsString()
  @MaxLength(180)
  search?: string;

  @IsOptional()
  @Transform(trim)
  @IsString()
  @MaxLength(20)
  level?: string;

  @IsOptional()
  @IsUUID()
  courseId?: string;

  @IsOptional()
  @IsUUID()
  examTemplateId?: string;

  @IsOptional()
  @IsEnum(EvaluationQueueSortBy)
  sortBy?: EvaluationQueueSortBy;

  @IsOptional()
  @IsEnum(EvaluationQueueSortOrder)
  sortOrder?: EvaluationQueueSortOrder;

  @IsOptional()
  @Type(() => Number)
  @IsInt()
  @Min(1)
  page?: number;

  @IsOptional()
  @Type(() => Number)
  @IsInt()
  @Min(1)
  @Max(100)
  limit?: number;
}

export class GiveFinalVerdictDto {
  @Type(() => Number)
  @IsInt()
  @Min(0)
  @Max(100)
  vocabularyUsageScore: number;

  @Type(() => Number)
  @IsInt()
  @Min(0)
  @Max(100)
  grammarAccuracyScore: number;

  @Type(() => Number)
  @IsInt()
  @Min(0)
  @Max(100)
  fluencyPronunciationScore: number;

  @IsOptional()
  @Type(() => Number)
  @IsNumber()
  @Min(0)
  @Max(10)
  writingScore?: number;

  @IsOptional()
  @Type(() => Number)
  @IsNumber()
  @Min(0)
  @Max(10)
  speakingScore?: number;

  /*
   * Kept for compatibility with an older frontend.
   * The backend calculates the final score.
   */
  @IsOptional()
  @Type(() => Number)
  @IsNumber()
  @Min(0)
  @Max(100)
  finalAverageScore?: number;

  @Transform(trim)
  @IsString()
  teacherComment: string;

  @IsOptional()
  @Transform(trim)
  @IsString()
  teacherCommentBn?: string;

  @IsOptional()
  @Transform(trim)
  @IsString()
  keyStrength?: string;

  @IsOptional()
  @Transform(trim)
  @IsString()
  criticalGap?: string;

  @IsEnum(ExamVerdict)
  verdict: ExamVerdict;

  @IsOptional()
  @Type(() => Number)
  @IsInt()
  @Min(0)
  evaluationDurationMinutes?: number;

  @IsOptional()
  @Type(() => Number)
  @IsInt()
  @Min(0)
  @Max(100)
  scoreReliabilityPercent?: number;

  @IsOptional()
  @IsBoolean()
  issueCertificate?: boolean;

  /*
   * Legacy field. New PDFs are generated by the backend.
   * Keeping it prevents older requests from failing whitelist validation.
   */
  @IsOptional()
  @IsUUID()
  pdfFileId?: string;

  @IsOptional()
  @IsBoolean()
  notifyStudent?: boolean;
}

export class RequestRetakeDto {
  @IsOptional()
  @Transform(trim)
  @IsString()
  keyStrength?: string;

  @Transform(trim)
  @IsString()
  criticalGap: string;

  @Transform(trim)
  @IsString()
  teacherComment: string;

  @IsOptional()
  @Transform(trim)
  @IsString()
  teacherCommentBn?: string;

  @IsOptional()
  @IsBoolean()
  notifyStudent?: boolean;
}

export class IssueCertificateDto {
  @IsOptional()
  @IsUUID()
  pdfFileId?: string;

  @IsOptional()
  @IsBoolean()
  notifyStudent?: boolean;
}

export class ReopenEvaluationDto {
  @IsOptional()
  @Transform(trim)
  @IsString()
  @MaxLength(500)
  reason?: string;
}
